int main()
{
    // this is a declaration of
    // initialization of the variable
    int i;

    // this a definition of the
    // variable
    i = 7;

    // this allows for doing
    // both declare and define
    // a variable at the same time
    int j = 7;

    // const here is qualifier,
    // that qualify the variable
    // as a constant that cannot
    // be changed
    const int k = 42;

    return 0;
}